<?php
// Створення масиву з 10 випадкових чисел
$numbers = [];

for ($i = 0; $i < 10; $i++) {
    $numbers[] = rand(1, 100);
}

// Виведення всіх чисел
echo "Масив чисел: " . implode(", ", $numbers) . "<br>";

// Виведення мінімального і максимального значення
echo "Мінімальне значення: " . min($numbers) . "<br>";
echo "Максимальне значення: " . max($numbers);
?>
